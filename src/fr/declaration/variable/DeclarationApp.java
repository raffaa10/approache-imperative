package fr.declaration.variable;

public class DeclarationApp {

	public static void main(String[] args) {
		
		byte numeroDepartement = 34;
		short numeroRue = 15;
		int codePostal = 34_000;
		long numeroHabitants = 25000L;
		float moyenneParRue = 1.5f;
		String nomRue = "Bonnes Nouvelles";
		boolean presence = true; 
		
		// imprimer les variables
		
		System.out.println(numeroDepartement);
		System.out.println(numeroRue);
		System.out.println(codePostal);
		System.out.println(numeroHabitants);
		System.out.println(moyenneParRue);
		System.out.println(nomRue);
		System.out.println(presence);
		

	}

}
